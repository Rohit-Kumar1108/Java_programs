public class AssignmentOperator {
    public static void main(String[] args) {
        

        int a=10;
        int b;

        // = Operator 
        b=a;
        System.out.println(b);  // output = 10

        // += Operator  
        a+=b;  // (a+=b) = (a=a+b)
        System.out.println(a);  // output = 20


         // -= Operator  
         a-=b;  // (a -= b) = (a = a-b)
         System.out.println(a); // output = 10


         // *= Operator  
         a *= b;  // (a *= b) = (a = a*b)
         System.out.println(a); // output 100
    }
    
}


