public class RelationalOperator {
    public static void main(String[] args) {
        int a=10;
        int b=15;

        System.out.println(a==b); //false
        System.out.println(a!=b); //true
        System.out.println(a<=b); //true
        System.out.println(a>=b); //false
        System.out.println(a<b); //true
        System.out.println(a>b); //false
    }
    
}


// Relational Operator 
// == (Equals to )
// ! (Not Equals to)
// > (Greater than)
// < (Less than )
// >= (Greater than Equal to)
// <= (Lesser than Equal to)