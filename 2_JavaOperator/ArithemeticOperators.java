public class ArithemeticOperators {

    public static void main(String[] args) {
        int p=15;  
        int q=7;                                                  
                                                               
        System.out.println(p+q); 
        System.out.println(p-q);
        System.out.println(p*q);
        System.out.println(p/q);
        System.out.println(p%q);

    }
}

 //  Arithemetic Operator 
//   + , - , * , / , % 
